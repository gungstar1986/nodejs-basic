const os = require("os");

// PC platform
console.log(os.platform());

// PC CPU
console.log(os.arch());

// PC info
console.log(os.cpus());

// PC RAM
console.log(os.freemem());

// Total RAM
console.log(os.totalmem());

// PC parent directory
console.log(os.homedir());

// PC uptime
console.log(os.uptime());
